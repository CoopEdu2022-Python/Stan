'''
1.
面向过程
所谓面向过程就是根据需求逐步开发，然后封装到函数里面
然后按顺序调用函数
面向过程对于开发复杂的项目来说，没有固定的套路，开发难度就会很大

面向对象
面向对象的侧重点是谁来做
首先确定职责
根据职责确定不同的对象然后在对象内封装方法
最后根据顺序让不同的对象调用不同的方法
'''

'''
2.
面向对象的出现是为了解决复杂项目的开发，如果一步一步调用函数特别复杂，
封装到对象里面之后会降低代码复杂度，让逻辑更加清晰
'''

